from typing import List, Tuple


# @lc code=start
class Solution:
    def __init__(self) -> None:
        self.visited = []

    def maxAreaOfIsland(self, grid: List[List[int]]) -> int:
        R, C, maximum = len(grid), len(grid[0]), 0
        for r in range(R):
            for c in range(C):
                if grid[r][c] == 1:
                    maximum = max(self.bfs(grid, R, C, (r, c)), maximum)
        return maximum

    def bfs(self, grid: List[List[int]], R: int, C: int,
            coord: Tuple[int, int]) -> int:
        queue, size = [], 0
        queue.append(coord)
        while queue:
            row, col = queue.pop(0)
            self.visited.append((row, col))
            size += 1
            u, d = row - 1, row + 1
            l, r = col - 1, col + 1
            if u >= 0 and (u, col) not in self.visited and grid[u][col]:
                if (u, col) not in queue:
                    queue.append((u, col))
            if d < R and (d, col) not in self.visited and grid[d][col]:
                if (d, col) not in queue:
                    queue.append((d, col))
            if l >= 0 and (row, l) not in self.visited and grid[row][l]:
                if (row, l) not in queue:
                    queue.append((row, l))
            if r < C and (row, r) not in self.visited and grid[row][r]:
                if (row, r) not in queue:
                    queue.append((row, r))
        return size


